export const buildGoogleFontStylesheetUrl = (fontKeys: GoogleFontKey[]) => {
  const uniqueQueries = Array.from(
    new Set(
      fontKeys
        .map((key) => googleFontCatalog[key]?.query)
        .filter((query): query is string => Boolean(query))
    )
  );

  return `https://fonts.googleapis.com/css2?${uniqueQueries
    .map((query) => `family=${query}`)
    .join('&')}&display=swap`;
};

